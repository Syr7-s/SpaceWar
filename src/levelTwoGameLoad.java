    private void gameLoadLevelTwoBtnMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_gameLoadLevelTwoBtnMouseClicked
        // TODO add your handling code here:
        Timer timer = new Timer(75, null);
        timer.addActionListener((ActionEvent e) -> {
            counter += 5;
            gameLoadLevelTwoText.setText("Level II is loading...");
            gameLoadLevelTwoGif.setIcon(new javax.swing.ImageIcon(getClass().getResource("/images/Infinity.gif")));
            if (counter == 400) {
                timer.stop();
                try {
                    JOptionPane.showMessageDialog(rootPane, "Level II loaded","Oyun Yukleme",JOptionPane.INFORMATION_MESSAGE);
                    new gameSelection().gamePlayLevelTwo();
                    setVisible(false);
                } catch (Exception ex) {
                    JOptionPane.showMessageDialog(null, "Unexpected  a situation has been occurred");
                }               
            }
        });
        timer.start();
    }//GEN-LAST:event_gameLoadLevelTwoBtnMouseClicked

